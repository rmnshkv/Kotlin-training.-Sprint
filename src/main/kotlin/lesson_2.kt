fun main() {
    val kmh = 112
    val min = 60
    val dist = (kmh / min)
    println(dist)

    val intKmh: Int = 112
    val intMin: Int = 60
    println(intKmh / intMin)

    val floatKmh: Float = 112f
    val floatMin: Float = 60f
    println(floatKmh / floatMin)

    val doubleKmh: Double = 112.0
    val doubleMin: Double = 60.0
    println(doubleKmh / doubleMin)
}
